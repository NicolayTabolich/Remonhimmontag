// –§—É–Ω–∫—Ü–∏—è –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
function init() {
   renderLatestNews(); // –û—Ç—Ä–∏—Å–æ–≤–∫–∞ –ø–æ—Å–ª–µ–¥–Ω–∏—Ö –Ω–æ–≤–æ—Å—Ç–µ–π
   renderNews(); // –û—Ç—Ä–∏—Å–æ–≤–∫–∞ –≤—Å–µ—Ö –Ω–æ–≤–æ—Å—Ç–µ–π
   setupFilters(); // –ù–∞—Å—Ç—Ä–æ–π–∫–∞ —Ñ–∏–ª—å—Ç—Ä–æ–≤
   setupEventListeners(); // –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ —Å–æ–±—ã—Ç–∏–π
   setupSwipeHandlers(); // –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ —Å–≤–∞–π–ø–æ–≤
   populateYearFilter(); // –ó–∞–ø–æ–ª–Ω–µ–Ω–∏–µ —Ñ–∏–ª—å—Ç—Ä–∞ –≥–æ–¥–æ–≤
}

// –§—É–Ω–∫—Ü–∏—è –∑–∞–ø–æ–ª–Ω–µ–Ω–∏—è —Ñ–∏–ª—å—Ç—Ä–∞ –≥–æ–¥–æ–≤
function populateYearFilter() {
   const years = new Set();
   newsData.forEach(news => {
      const year = new Date(news.date).getFullYear();
      years.add(year);
   });

   const sortedYears = Array.from(years).sort((a, b) => b - a);

   sortedYears.forEach(year => {
      const option = document.createElement('option');
      option.value = year;
      option.textContent = year;
      yearFilter.appendChild(option);
   });
}

// –§—É–Ω–∫—Ü–∏—è –æ—Ç—Ä–∏—Å–æ–≤–∫–∏ –ø–æ—Å–ª–µ–¥–Ω–∏—Ö –Ω–æ–≤–æ—Å—Ç–µ–π (3 —Å–∞–º—ã–µ —Å–≤–µ–∂–∏–µ)
function renderLatestNews() {
   // –û—á–∏—Å—Ç–∫–∞ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞
   latestNewsContainer.innerHTML = '';

   // –°–æ—Ä—Ç–∏—Ä—É–µ–º –Ω–æ–≤–æ—Å—Ç–∏ –ø–æ –¥–∞—Ç–µ (–æ—Ç –Ω–æ–≤—ã—Ö –∫ —Å—Ç–∞—Ä—ã–º) –∏ –±–µ—Ä–µ–º –ø–µ—Ä–≤—ã–µ 3
   const latestNews = [...newsData]
      .sort((a, b) => new Date(b.date) - new Date(a.date))
      .slice(0, 3);

   // –°–æ–∑–¥–∞–Ω–∏–µ –∫–∞—Ä—Ç–æ—á–µ–∫ –ø–æ—Å–ª–µ–¥–Ω–∏—Ö –Ω–æ–≤–æ—Å—Ç–µ–π
   latestNews.forEach(news => {
      const newsCard = document.createElement('div');
      newsCard.className = 'latest-news-card';
      newsCard.setAttribute('data-id', news.id);

      // –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –¥–∞—Ç—ã
      const date = new Date(news.date);
      const formattedDate = date.toLocaleDateString('ru-RU', {
         year: 'numeric',
         month: 'long',
         day: 'numeric'
      });

      // –ó–∞–ø–æ–ª–Ω—è–µ–º –∫–∞—Ä—Ç–æ—á–∫—É —Å–æ–¥–µ—Ä–∂–∏–º—ã–º
      newsCard.innerHTML = `
                    <img src="${news.images[0]}" alt="${news.title}" class="latest-news-image">
                    <div class="latest-news-content">
                        <div class="latest-news-date">${formattedDate}</div>
                        <h3 class="latest-news-title">${news.title}</h3>
                        <p class="latest-news-summary">${news.summary}</p>
                    </div>
                `;

      // –î–æ–±–∞–≤–ª—è–µ–º –∫–∞—Ä—Ç–æ—á–∫—É –≤ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
      latestNewsContainer.appendChild(newsCard);
   });
}

// –§—É–Ω–∫—Ü–∏—è –æ—Ç—Ä–∏—Å–æ–≤–∫–∏ –≤—Å–µ—Ö –Ω–æ–≤–æ—Å—Ç–µ–π
function renderNews() {
   newsContainer.innerHTML = '';

   const startIndex = (currentPage - 1) * newsPerPage;
   const endIndex = startIndex + newsPerPage;
   const newsToShow = filteredNews.slice(startIndex, endIndex);

   if (newsToShow.length === 0) {
      newsContainer.innerHTML = '<p style="grid-column: 1 / -1; text-align: center; padding: 40px;">–ù–æ–≤–æ—Å—Ç–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã</p>';
      paginationContainer.innerHTML = '';
      return;
   }

   newsToShow.forEach(news => {
      const newsCard = document.createElement('div');
      newsCard.className = 'news-card';
      newsCard.setAttribute('data-id', news.id);

      const date = new Date(news.date);
      const formattedDate = date.toLocaleDateString('ru-RU', {
         year: 'numeric',
         month: 'long',
         day: 'numeric'
      });

      // –ó–∞–ø–æ–ª–Ω—è–µ–º –∫–∞—Ä—Ç–æ—á–∫—É —Å–æ–¥–µ—Ä–∂–∏–º—ã–º
      newsCard.innerHTML = `
                    <img src="${news.images[0]}" alt="${news.title}" class="news-image">
                    <div class="news-content">
                        <div class="news-date">${formattedDate}</div>
                        <h3 class="news-title">${news.title}</h3>
                        <p class="news-summary">${news.summary}</p>
                    </div>
                `;

      newsContainer.appendChild(newsCard);
   });

   renderPagination();
}

function renderPagination() {
   const totalPages = Math.ceil(filteredNews.length / newsPerPage);

   paginationContainer.innerHTML = '';

   if (totalPages < 2) return;

   for (let i = 1; i <= totalPages; i++) {
      const pageBtn = document.createElement('button');
      pageBtn.className = `page-btn ${i === currentPage ? 'active' : ''}`;
      pageBtn.textContent = i;
      pageBtn.addEventListener('click', () => {
         currentPage = i;
         renderNews();

         // –ü—Ä–æ–∫—Ä—É—Ç–∫–∞ –≤–≤–µ—Ä—Ö –ø—Ä–∏ —Å–º–µ–Ω–µ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
         window.scrollTo({
            top: 0,
            behavior: 'smooth'
         });
      });

      paginationContainer.appendChild(pageBtn);
   }
}

function setupFilters() {
   yearFilter.addEventListener('change', applyFilters);
   monthFilter.addEventListener('change', applyFilters);
}

function applyFilters() {
   const selectedYear = yearFilter.value;
   const selectedMonth = monthFilter.value;

   currentPage = 1;

   filteredNews = newsData.filter(news => {
      const newsDate = new Date(news.date);
      const newsYear = newsDate.getFullYear().toString();
      const newsMonth = (newsDate.getMonth() + 1).toString();

      const yearMatch = selectedYear === 'all' || newsYear === selectedYear;
      const monthMatch = selectedMonth === 'all' || newsMonth === selectedMonth;

      return yearMatch && monthMatch;
   });

   renderNews();

   // –ü—Ä–æ–∫—Ä—É—Ç–∫–∞ –≤–≤–µ—Ä—Ö –ø—Ä–∏ –ø—Ä–∏–º–µ–Ω–µ–Ω–∏–∏ —Ñ–∏–ª—å—Ç—Ä–æ–≤
   window.scrollTo({
      top: 0,
      behavior: 'smooth'
   });
}

function setupSwipeHandlers() {
   setupSwipeForElement(news_news_slider, handlenews_news_sliderSwipe);
   setupSwipeForElement(fullscreenImage, handleFullscreenSwipe);
}

function setupSwipeForElement(element, callback) {
   element.addEventListener('touchstart', (e) => {
      startX = e.touches[0].clientX;
      currentX = startX;
      isSwiping = true;
   }, { passive: true });

   element.addEventListener('touchmove', (e) => {
      if (!isSwiping) return;
      currentX = e.touches[0].clientX;
   }, { passive: true });

   element.addEventListener('touchend', (e) => {
      if (!isSwiping) return;

      const diffX = startX - currentX;
      const minSwipeDistance = 50;

      if (Math.abs(diffX) > minSwipeDistance) {
         if (diffX > 0) {
            callback(1);
         } else {
            callback(-1);
         }
      }

      isSwiping = false;
   }, { passive: true });
}

function handlenews_news_sliderSwipe(direction) {
   changenews_slide(direction);
}

