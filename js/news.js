// –§—É–Ω–∫—Ü–∏—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –¥–∞—Ç—ã –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —è–∑—ã–∫–∞
function formatDate(dateString) {
   const date = new Date(dateString);
   const day = date.getDate();
   const year = date.getFullYear();
   let month;

   if (currentLanguage === 'be') {
      // –ú–µ—Å—è—Ü—ã –Ω–∞ –±–µ–ª–æ—Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ –≤ —Ä–æ–¥–∏—Ç–µ–ª—å–Ω–æ–º –ø–∞–¥–µ–∂–µ
      const monthsBe = [
         '—Å—Ç—É–¥–∑–µ–Ω—è', '–ª—é—Ç–∞–≥–∞', '—Å–∞–∫–∞–≤—ñ–∫–∞', '–∫—Ä–∞—Å–∞–≤—ñ–∫–∞',
         '–º–∞—è', '—á—ç—Ä–≤–µ–Ω—è', '–ª—ñ–ø–µ–Ω—è', '–∂–Ω—ñ—û–Ω—è',
         '–≤–µ—Ä–∞—Å–Ω—è', '–∫–∞—Å—Ç—Ä—ã—á–Ω—ñ–∫–∞', '–ª—ñ—Å—Ç–∞–ø–∞–¥–∞', '—Å–Ω–µ–∂–Ω—è'
      ];
      month = monthsBe[date.getMonth()];
   } else {
      // –ú–µ—Å—è—Ü—ã –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ –≤ —Ä–æ–¥–∏—Ç–µ–ª—å–Ω–æ–º –ø–∞–¥–µ–∂–µ
      const monthsRu = [
         '—è–Ω–≤–∞—Ä—è', '—Ñ–µ–≤—Ä–∞–ª—è', '–º–∞—Ä—Ç–∞', '–∞–ø—Ä–µ–ª—è',
         '–º–∞—è', '–∏—é–Ω—è', '–∏—é–ª—è', '–∞–≤–≥—É—Å—Ç–∞',
         '—Å–µ–Ω—Ç—è–±—Ä—è', '–æ–∫—Ç—è–±—Ä—è', '–Ω–æ—è–±—Ä—è', '–¥–µ–∫–∞–±—Ä—è'
      ];
      month = monthsRu[date.getMonth()];
   }

   return `${day} ${month} ${year} –≥–æ–¥–∞`;
}

